package Funcionarios;

public class Funcionarios {
    String nome;
    int idade;
    double salario;


    void setNome(String nome){

        this.nome = nome;
    }

    String getNome(){

        return this.nome;
    }

    void setIdade(int idade){

        this.idade = idade;

    }

    int getIdade(){
        return this.idade;
    }

    void setSalario(double salario){
        this.salario = salario;
    }
}

    
